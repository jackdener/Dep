    private void botaoParesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoParesActionPerformed
        int digito = Integer.parseInt(JOptionPane.showInputDialog(this, "Digite um número inteiro positivo!"));
        String resultado = "Os números pares de 0 até " + digito + " são:\n";
        for (int i = 0; i <= digito; i++) {
            if (i % 2 == 0) {
                resultado += "\n" + i;
            }
        }
        JOptionPane.showMessageDialog(this, resultado);
    }//GEN-LAST:event_botaoParesActionPerformed

    private void botaoPrimosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPrimosActionPerformed
        int digito = Integer.parseInt(JOptionPane.showInputDialog(this, "Digite um número inteiro positivo!"));
        String resultado = "Os números primos de 0 até " + digito + " são:\n";
        for (int i = 1; i <= digito; i++) {
            int divisores = 0;
            for (int j = 1; j <= i; j++) {
                if (i % j == 0) {
                    divisores ++;
                }
            }
            if(divisores == 2){
                resultado += "\n" + i + " é primo!";
            }
        }
        JOptionPane.showMessageDialog(this, resultado);
    }//GEN-LAST:event_botaoPrimosActionPerformed

