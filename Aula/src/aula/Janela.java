    public static void escolha() {
        int n = Integer.parseInt(JOptionPane.showInputDialog("Digite 1 para calculadora de media\nDigite 2 para calculadora de formula de bhaskara\nDigite 3 para manipular strings\nDigite 0 para sair"));
        if (n == 1) {
            calcMedia();
        } else if (n == 2) {
            calcformuladebhaskara();
        } else if (n == 3) {
            manipulaString();
        } else if (n == 0) {
            JOptionPane.showMessageDialog(null, "Volte sempre!");
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma opção valida");
            zoeiras();
        }
    }

    public static void calcMedia() {
        int qtd = Integer.parseInt(JOptionPane.showInputDialog("Digite a quantidade de numeros que deseja calcular a média."));
        float soma = 0;
        for (int i = 1; i <= qtd; i++) {
            soma = soma + Integer.parseInt(JOptionPane.showInputDialog("digite o " + i + " numero!"));
        }
        float media = soma / qtd;
        JOptionPane.showMessageDialog(null, soma);

    }

    public static void calcformuladebhaskara() {
        int a = Integer.parseInt(JOptionPane.showInputDialog("digite o valor de a."));
        int b = Integer.parseInt(JOptionPane.showInputDialog("digite o valor de b."));
        int c = Integer.parseInt(JOptionPane.showInputDialog("digite o valor de c."));
        double delta = b * b - 4 * a * c;
        double x1 = (-b + Math.sqrt(delta)) / 2 * a;
        double x2 = (-b - Math.sqrt(delta)) / 2 * a;
        JOptionPane.showMessageDialog(null, "resposta: " + x1);
        JOptionPane.showMessageDialog(null, "resposta: " + x2);

    }

    public static void manipulaString() {
        int num = Integer.parseInt(JOptionPane.showInputDialog("Digite um numero inteiro"));
        String mensagem = "o numero digitado é ";
        if (num < 0) {
            mensagem += "positivo";
        } else if (num > 0) {
            mensagem += "negativo";
        } else {
            mensagem += "neutro";
        }
        if (num % 2 == 0) {
            mensagem += " e par";
        } else {
            mensagem += " e ímpar";
        }
        JOptionPane.showMessageDialog(null, mensagem);
    }

    public static void zoeiras() {
        String sacanagem = "oi";
        for (int i = 1; i < 2; i--) {
            sacanagem += JOptionPane.showInputDialog(sacanagem);
            if (sacanagem.contains("palmeiras não tem mundial")) {
                i = 3;
            }
        }
        JOptionPane.showMessageDialog(null, "isso mesmo!");
        escolha();

    }

